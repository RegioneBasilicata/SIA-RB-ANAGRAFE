package it.csi.solmr.util;

public class Page{
  private String time;
  private String code;
  public Page() {
  }
  public String getTime() {
    return time;
  }
  public void setTime(String time) {
    this.time = time;
  }
  public String getCode() {
    return code;
  }
  public void setCode(String code) {
    this.code = code;
  }

}
